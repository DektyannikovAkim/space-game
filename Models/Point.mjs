export class Point {
  /**
   * @param {number} x
   * @param {number} y
   * @param {number} velX
   * @param {number} velY
   * @param {number} size
   */
  constructor(x, y, velX, velY, size) {
    this.x = x;
    this.y = y;
    this.velX = velX;
    this.velY = velY;
    this.size = size;
  }
}
